#!/usr/bin/env python3
"""
hybrid_simulation_helper.py
============================
Replaces opencor_helper.SimulationHelper for DAE models where OpenCOR's
CVODES linear solver fails with:
    "the linear solver's setup function failed in an unrecoverable manner"

Root cause of that error:
    CVODES/IDA tries to factorise the full DAE Jacobian, which includes the
    circular algebraic block:
        flow → resistance → viscosity → H_mean → RBC_volume → FQB → flow
    This block's Jacobian is dense and ill-conditioned, so IDA's LU
    factorisation fails.

Fix:
    1.  scipy.integrate.solve_ivp(method='BDF') handles the ODE states.
        This is the same BDF algorithm as CVODES — just Python-callable.
    2.  scipy.optimize.root replaces rootfind_0 inside computeRates.
        It solves the 61-variable iterative algebraic system BEFORE the
        ODE solver ever tries to touch it, so the Jacobian CVODES sees
        is clean.

API mirrors opencor_helper.SimulationHelper so the calling code in
PP_VP_VV_PP_PP.py / run_B1_I.py needs minimal changes.
"""

